#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int r;

	if (fd < 0)
		return ;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		r = n % 10;
		if (n / 10 != 0)
		{
			n = n / 10;
			ft_putnbr_fd(n, fd);
		}
		ft_putchar_fd(r + '0', fd);
	}
}
